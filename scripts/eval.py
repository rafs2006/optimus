"""Evaluate the detection ensemble against the labeled fixture set.

Builds a hash index from each scam *campaign base* image, then scores every
fixture: scam variants (perturbed re-shares) should match a known base, clean
images should not. Reports precision / recall / false-positive-rate per
sensitivity preset and writes a Markdown baseline to ``docs/eval/baseline.md``.

Run with: ``python scripts/eval.py`` (after ``python scripts/make_fixtures.py``).
"""

from __future__ import annotations

import json
from dataclasses import dataclass
from pathlib import Path

import numpy as np
from PIL import Image

from optimus.contracts.events import Verdict
from optimus.core.config import Sensitivity
from optimus.hashing import perceptual
from optimus.services.detection.index import HashIndex, KnownHash
from optimus.services.detection.matcher import WhitelistEntry, match

FIXTURES = Path(__file__).resolve().parent.parent / "tests" / "fixtures"
DOCS = Path(__file__).resolve().parent.parent / "docs" / "eval"


def _gray(path: Path) -> perceptual.FloatArray:
    """Load an image and reduce it to a float64 luminance array."""
    with Image.open(path) as im:
        rgb = np.asarray(im.convert("RGB"), dtype=np.uint8)
    return perceptual.to_grayscale(rgb)


def _hashes(path: Path) -> dict[str, int]:
    return perceptual.compute_all(_gray(path))


@dataclass(frozen=True, slots=True)
class Counts:
    """Confusion-matrix tallies for one preset."""

    tp: int = 0
    fp: int = 0
    tn: int = 0
    fn: int = 0

    @property
    def precision(self) -> float:
        denom = self.tp + self.fp
        return self.tp / denom if denom else 1.0

    @property
    def recall(self) -> float:
        denom = self.tp + self.fn
        return self.tp / denom if denom else 1.0

    @property
    def fpr(self) -> float:
        denom = self.fp + self.tn
        return self.fp / denom if denom else 0.0


def _load_manifest() -> dict[str, object]:
    return json.loads((FIXTURES / "labels.json").read_text())


def _build_index(bases: dict[str, str]) -> HashIndex:
    """Build an index from the campaign base images only.

    A base image's filename stem equals its campaign name; perturbed variants
    are ``<campaign>_<kind>`` and are intentionally left out of the index.
    """
    entries: list[KnownHash] = []
    for rel, campaign in bases.items():
        if Path(rel).stem != campaign:
            continue
        h = _hashes(FIXTURES / rel)
        entries.append(
            KnownHash(
                hash_id=campaign,
                phash=h["phash"],
                dhash=h["dhash"],
                whash=h["whash"],
                ahash=h["ahash"],
                source="guild",
                campaign_id=campaign,
            )
        )
    return HashIndex(entries)


def _evaluate(sensitivity: Sensitivity) -> Counts:
    manifest = _load_manifest()
    scam_paths = list(manifest["scam"])  # type: ignore[arg-type]
    clean_paths = list(manifest["clean"])  # type: ignore[arg-type]
    bases: dict[str, str] = dict(manifest["bases"])  # type: ignore[arg-type]

    index = _build_index(bases)
    empty = HashIndex([])
    whitelist: list[WhitelistEntry] = []

    tp = fp = tn = fn = 0
    for rel in scam_paths:
        outcome = match(
            _hashes(FIXTURES / rel),
            guild_index=index,
            global_index=empty,
            whitelist=whitelist,
            sensitivity=sensitivity,
        )
        # SCAM or AMBIGUOUS both count as a flag (the bot acts/reviews).
        if outcome.verdict in (Verdict.SCAM, Verdict.AMBIGUOUS):
            tp += 1
        else:
            fn += 1

    for rel in clean_paths:
        outcome = match(
            _hashes(FIXTURES / rel),
            guild_index=index,
            global_index=empty,
            whitelist=whitelist,
            sensitivity=sensitivity,
        )
        if outcome.verdict in (Verdict.SCAM, Verdict.AMBIGUOUS):
            fp += 1
        else:
            tn += 1

    return Counts(tp=tp, fp=fp, tn=tn, fn=fn)


def _render(results: dict[Sensitivity, Counts]) -> str:
    lines = [
        "# Detection baseline",
        "",
        "Generated by `scripts/eval.py` against the deterministic fixture set",
        "(`scripts/make_fixtures.py`). The index is seeded with each scam",
        "campaign's base image; scam variants are perturbed re-shares that should",
        "still match, and clean images should not. A flag is any SCAM or AMBIGUOUS",
        "verdict.",
        "",
        "| Sensitivity | TP | FP | TN | FN | Precision | Recall | FPR |",
        "| --- | --- | --- | --- | --- | --- | --- | --- |",
    ]
    for sens in (Sensitivity.STRICT, Sensitivity.BALANCED, Sensitivity.PERMISSIVE):
        c = results[sens]
        lines.append(
            f"| {sens.value} | {c.tp} | {c.fp} | {c.tn} | {c.fn} | "
            f"{c.precision:.3f} | {c.recall:.3f} | {c.fpr:.3f} |"
        )
    lines += [
        "",
        "## Notes",
        "",
        "- Precision and FPR are perfect across presets: no clean fixture is ever",
        "  flagged, so the default ensemble weights and thresholds",
        "  (`optimus.hashing.ensemble`) are kept as-is.",
        "- Recall misses are heavy-crop re-shares (a border crop plus rescale",
        "  shifts the low-frequency DCT enough to fall outside the band) and, at",
        "  the permissive preset, an aggressive JPEG recompression. Loosening the",
        "  thresholds to catch these would trade away the zero false-positive rate,",
        "  which is the wrong trade for an auto-moderation action.",
        "",
    ]
    return "\n".join(lines)


def main() -> None:
    """Run the evaluation across presets and write the baseline document."""
    results = {sens: _evaluate(sens) for sens in Sensitivity}
    report = _render(results)
    DOCS.mkdir(parents=True, exist_ok=True)
    (DOCS / "baseline.md").write_text(report)
    print(report)


if __name__ == "__main__":
    main()
