"""Render load-test results as a stdout/Markdown summary table and JSON."""

from __future__ import annotations

import json
from collections.abc import Sequence
from typing import Any

from benchmarks.load.runner import LoadResult

_HEADER = (
    "| Concurrency | Images | Wall (s) | Images/s | p50 (ms) | p95 (ms) | "
    "p99 (ms) | mean (ms) | max (ms) | Peak RSS (MB) |"
)
_DIVIDER = "| --- | --- | --- | --- | --- | --- | --- | --- | --- | --- |"


def _row(r: LoadResult) -> str:
    return (
        f"| {r.concurrency} | {r.images} | {r.wall_seconds:.2f} | "
        f"{r.images_per_sec:.1f} | {r.p50_ms:.1f} | {r.p95_ms:.1f} | "
        f"{r.p99_ms:.1f} | {r.mean_ms:.1f} | {r.max_ms:.1f} | {r.peak_rss_mb:.1f} |"
    )


def render_table(results: Sequence[LoadResult]) -> str:
    """Render the per-concurrency-level results as a Markdown table."""
    return "\n".join([_HEADER, _DIVIDER, *(_row(r) for r in results)])


def render_report(results: Sequence[LoadResult], *, corpus_images: int) -> str:
    """Render the full human-readable load report as Markdown."""
    lines = [
        "# Detection throughput load test",
        "",
        "Generated by `python -m benchmarks.load`. Each row pushes the given",
        "number of synthetic images through the real detection worker (sandboxed",
        "decode subprocess + perceptual hashing + phash-index match + ensemble vote)",
        "with the stated number concurrently in flight. Images/s is the image",
        "count over the wall time of the whole run; latency is per-image",
        "end-to-end (worker `handle`).",
        "",
        f"Corpus: {corpus_images} distinct images, cycled to reach each run's size.",
        "",
        render_table(results),
        "",
    ]
    return "\n".join(lines)


def build_json(results: Sequence[LoadResult], *, corpus_images: int) -> dict[str, Any]:
    """Build a JSON-serializable summary of the load run."""
    return {
        "corpus_images": corpus_images,
        "levels": [
            {
                "concurrency": r.concurrency,
                "images": r.images,
                "wall_seconds": r.wall_seconds,
                "images_per_sec": r.images_per_sec,
                "p50_ms": r.p50_ms,
                "p95_ms": r.p95_ms,
                "p99_ms": r.p99_ms,
                "mean_ms": r.mean_ms,
                "max_ms": r.max_ms,
                "peak_rss_mb": r.peak_rss_mb,
                "non_decisions": r.non_decisions,
            }
            for r in results
        ],
    }


def write_json(path: str, payload: dict[str, Any]) -> None:
    """Write ``payload`` to ``path`` as pretty-printed, sorted JSON."""
    with open(path, "w", encoding="utf-8") as fh:
        json.dump(payload, fh, indent=2, sort_keys=True)
        fh.write("\n")
